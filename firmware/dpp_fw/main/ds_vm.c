void binop(FUNC_PTR_BINOP bin_func)
{
  uint32_t rhs, lhs;
  stack_pop(&data_stack, &rhs);
  stack_pop(&data_stack, &lhs);
  stack_push(&data_stack, bin_func(lhs, rhs));
  stack_print(&data_stack, "AFTER BINOP");
}

